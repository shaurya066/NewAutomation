package practice1;

public class Switchcase {

	public static void main(String[] args) {
		int day=2;
		switch(day) 
		{
		
		case  1:
		System.out.println("sunday");
		case 2:
			System.out.println("monday");
		case 3:
			System.out.println("tuesday");
			break;
		case 4:
			System.out.println("wednesday");
			
			default:
				System.out.println("no data");
				
		
					
				
			}
		}

	}


